// Faça  um  algoritmo  que  calcule  e  mostre  a  área  de  um  trapézio.  Sabe-se  que:  
// A  =  (base maior + base menor)* altura)/2 ;

var baseMaior = 10       // 10cm²
var baseMenor = 6       // 6cm²
var altura = 4         // 4cm²

var areaTrapezio = ( ( baseMaior + baseMenor ) * altura ) / 2

console.log( ` Um trapézio possue ${areaTrapezio} cm² de área. ` )