// Faça  um  algoritmo  que  receba  o  peso  de  uma  pessoa  em  quilos,  
// calcule  e  mostre  esse peso em gramas.

// Para transformar de quilos quilos(Kg) para Gramas é só multiplicar o valor estipulado por 1000

var quilosPessoa = 90                           // Uma pessoa que possua 90 Kg
var pesoGramas =  ( quilosPessoa * 1000 )      // 1 kg = 1000 gramas

console.log(` Uma pessoa de ${quilosPessoa} Kg pesa em gramas ${pesoGramas} . `)
